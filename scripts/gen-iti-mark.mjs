// scripts/gen-iti-mark.mjs
//
// Dev-time generator for the venue-QR center mark asset. Renders the HappiTime
// "iTi" wordmark fragment (white, brand font, ExtraBold) to a crisp PNG and
// writes it base64-inlined into packages/venue-qr/iti-mark.mjs.
//
// Why inline base64 (not a .png file): the @happitime/venue-qr package is bundled
// into the Next.js route via transpilePackages. A pure-JS module with the bytes
// inline avoids runtime fs reads and bundler asset-emission entirely — the
// package keeps zero runtime deps beyond qrcode + pngjs.
//
// Font: Plus Jakarta Sans (OFL), the brand display face (see apps/web Logo.tsx,
//   metadataBase font --font-display). Variable TTF, ExtraBold (weight 800) instance:
//   https://github.com/google/fonts/raw/main/ofl/plusjakartasans/PlusJakartaSans%5Bwght%5D.ttf
// sharp is used ONLY here (authoring time); it is already present in the repo's
// node_modules. It is NOT a dependency of the published package.
//
// Regenerate:  node scripts/gen-iti-mark.mjs
//   (downloads the font to a temp path if not already cached there)

import { mkdir, writeFile, readFile, access } from 'node:fs/promises';
import path from 'node:path';
import os from 'node:os';
import sharp from 'sharp';

const FONT_URL =
  'https://github.com/google/fonts/raw/main/ofl/plusjakartasans/PlusJakartaSans%5Bwght%5D.ttf';
const FONT_PATH = path.join(os.tmpdir(), 'happitime-iti-PlusJakartaSans.ttf');
const OUT = path.resolve('packages/venue-qr/iti-mark.mjs');

async function ensureFont() {
  try {
    await access(FONT_PATH);
    return;
  } catch {
    /* not cached — fetch */
  }
  const res = await fetch(FONT_URL);
  if (!res.ok) throw new Error(`font download failed: ${res.status} ${FONT_URL}`);
  await mkdir(path.dirname(FONT_PATH), { recursive: true });
  await writeFile(FONT_PATH, Buffer.from(await res.arrayBuffer()));
}

async function main() {
  await ensureFont();

  // White "iTi" on a transparent background, box-fit to a generous height so the
  // committed asset downsamples crisply to any badge size. Only the alpha channel
  // is consumed at runtime (the mark is painted white over the brand disc), so the
  // foreground colour here is for human inspection of the asset.
  const png = await sharp({
    text: {
      text: '<span foreground="#FFFFFF">iTi</span>',
      font: 'Plus Jakarta Sans Ultra-Bold',
      fontfile: FONT_PATH,
      rgba: true,
      width: 1200,
      height: 440,
    },
  })
    .png()
    .toBuffer();

  const meta = await sharp(png).metadata();
  const b64 = png.toString('base64');

  const module = `// packages/venue-qr/iti-mark.mjs
// GENERATED by scripts/gen-iti-mark.mjs — do not edit by hand.
// White "iTi" brand mark (Plus Jakarta Sans ExtraBold), ${meta.width}x${meta.height}, RGBA PNG.
// Only the alpha channel is used at runtime; the mark is painted white over the brand disc.
export const ITI_MARK_PNG_BASE64 =
  "${b64}";
`;
  await writeFile(OUT, module);
  console.log(`✓ wrote ${path.relative(process.cwd(), OUT)} (${meta.width}x${meta.height}, ${b64.length} b64 chars)`);
}

main().catch((err) => {
  console.error('[gen-iti-mark] fatal:', err instanceof Error ? err.message : err);
  process.exit(1);
});
